// 안드로이드에서 화면을 그리는 방법
// -XML을 이용한다.
// -XML에 무엇?
// -DSL Language -> Domain Specific Language.
// -안드로이드 UI를 그리기 위해 특화된 언어임.


// 핸드폰마다 화면 크기가 다 다른데 어떻게 화면을 그려야 할까?
// - 픽셀, dpi, dp 단위
// - 픽셀 -> 핸드폰 화면에서 빛이 나오는 전구 -> 가장 작은 단위
// - dpi -> dot per inch
// - ldpi -> 120 (1인치에 120픽셀)
// - mdpi -> 160
// - hdpi -> 240
// - xhdpi -> 480
// - xxhdpi -> 640

// dp단위를 사용하면 어느 핸드폰에서나 동일하게 나옴
// dp -> Density Independet Pixel
// 핸드폰 해상도에 따라서 다르게 나올 수 있음.
// >> 어느 기종에서나 동일하게 나오게끔 단위를 통일함.

